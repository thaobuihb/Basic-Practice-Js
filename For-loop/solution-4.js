// Create a for loop to loop over a 2D array.
let a = [
  [0, 1, 2],
  [5, 10],
  [2, 4, 6],
];
for (let i = 0; i < a.length; i++) {
  for (let j = 0; j < a[i].length; j++) {
    console.log(a[i][j]);
  }
}

